void Map::render(SDL_Renderer* renderer) {
    SDL_Rect  destRect;
    SDL_Rect srcRect;

    // Duy?t qua t?t c? c�c � trong map
    for (int row = 0; row < MAP_HEIGHT / TILE_SIZE; row++) {
        for (int col = 0; col < MAP_WIDTH / TILE_SIZE; col++) {
            if (mapData[row][col] != 0) {
                destRect = { col * TILE_SIZE, row * TILE_SIZE, TILE_SIZE, TILE_SIZE };
                srcRect = { (mapData[row][col] - 1) * TILE_SIZE, 0, TILE_SIZE, TILE_SIZE };
                // V? t? tileset l�n m�n h�nh
                SDL_RenderCopy(renderer, texture, &srcRect, &destRect);
            }
        }
    }
}


bool Map::isSolidTile(int x, int y) {
    int col = x / TILE_SIZE;
    int row = y / TILE_SIZE;
    return mapData[row][col] != 0;
}


bool Map::isCollidingWithTile(const SDL_Rect& rect) {
    // Duy?t t?t c? c�c tile
    for (int row = 0; row < MAP_HEIGHT / TILE_SIZE; ++row) {
        for (int col = 0; col < MAP_WIDTH / TILE_SIZE; ++col) {
            if (mapData[row][col] != 0) {
                SDL_Rect tileRect = { col * TILE_SIZE, row * TILE_SIZE, TILE_SIZE, TILE_SIZE };
                // N?u h�nh ch? nh?t giao v?i tile n�y => c� va ch?m
                if (SDL_HasIntersection(&rect, &tileRect)) {
                    return true;
                }
            }
        }
    }
    // Kh�ng va ch?m
    return false;
}